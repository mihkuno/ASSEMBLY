
# Quiz 1.4
# What happened to the program counter and how many bits are incremented if an instructions is done?

# Answer: 4 bits

# Reason:
# MIPS instructions are each 4 bytes long, so the PC should be incremented by 4 to read the next instructions in sequence.
# In other words, it is incremented by 4 becuase memory is byte addressable and each instruction is 4 bytes long.
